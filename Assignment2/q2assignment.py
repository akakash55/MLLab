import matplotlib.pyplot as plt

x=[1,2,3,4]
y=[1,4,9,16]
print("Different plots with matplotlib library")
print("Line Plot")
plt.plot(x,y)
plt.show()
print("Bar Plot")
plt.bar(x,y)
plt.show()
print("Histogram")
plt.hist(y)
plt.show()
print("Scatter Plot")
plt.scatter(x,y)
plt.show()


