import numpy as np
arr = np.array([10,5,8,3,20,56,101,-1,90])

print(arr)

minElem = np.min(arr)
maxElem = np.max(arr)
totalSum = np.sum(arr)

print("Minimum element present in array: ",minElem)
print("Maximum element present in array: ",maxElem)
print("Total sum of elements present in array: ",totalSum)